#include "Duration.h"

#include "SamplingInfo.h"

namespace Music {

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
Duration::Duration( double noteDuration ) :
   m_noteDuration( noteDuration )
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getNumSamples
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
size_t Duration::getNumSamples( double bpm, const SamplingInfo& samplingInfo ) const
{
   return samplingInfo.convertSecsToSamples( getSeconds( bpm ) );
}

} /// namespace Music
