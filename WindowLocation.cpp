#include "WindowLocation.h"

namespace WaveAnalysis
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
WindowLocation::WindowLocation( size_t firstSampleIndex, size_t lastSampleIndex ) :
   m_first( firstSampleIndex ),
   m_last( lastSampleIndex )
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getFirstSample
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
size_t WindowLocation::getFirstSample() const
{
   return m_first;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getLastSample
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
size_t WindowLocation::getLastSample() const
{
   return m_last;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getCentre
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
size_t WindowLocation::getCentre() const
{
   return ( m_first + m_last ) / 2;
}

} /// namespace WaveAnalysis
