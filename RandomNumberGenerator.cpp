#include "RandomNumberGenerator.h"

#include "TRandom3.h"

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
RandomNumberGenerator::RandomNumberGenerator( size_t seed )
{
   m_ranGen = new TRandom3( seed );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
RandomNumberGenerator::~RandomNumberGenerator()
{
   delete m_ranGen;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// uniform
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double RandomNumberGenerator::uniform()
{
   return m_ranGen->Uniform();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// uniform
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double RandomNumberGenerator::uniform( double xMin, double xMax )
{
   return m_ranGen->Uniform( xMin, xMax );
}
