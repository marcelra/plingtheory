#include "IAccumArray.h"

#include "TwoTuple.h"

namespace Math
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// IAccumArray::Bin methods
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IAccumArray::Bin::Bin( double minX, double maxX ) :
   m_contents( 0 ),
   m_elements( 0 ),
   m_minX( minX ),
   m_maxX( maxX )
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IAccumArray::Bin::~Bin()
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// copy constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IAccumArray::Bin::Bin( const Bin& other ) :
   m_contents( other.m_contents ),
   m_elements( other.m_elements ),
   m_minX( other.m_minX ),
   m_maxX( other.m_maxX )
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// assignment operator
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IAccumArray::Bin& IAccumArray::Bin::operator=( const Bin& other )
{
   if ( this == &other )
   {
      return *this;
   }

   m_contents = other.m_contents;
   m_elements = other.m_elements;
   m_minX = other.m_minX;
   m_maxX = other.m_maxX;
   return *this;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// IAccumArray methods
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IAccumArray::~IAccumArray()
{}

} /// namespace Math
