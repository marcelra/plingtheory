#include "SamplingInfo.h"

#include <math.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
SamplingInfo::SamplingInfo()
{
   setSamplingRate( 44100 );
   setMaxValue( 32767 );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
SamplingInfo::SamplingInfo( double samplingRate, double maxValue )
{
   setSamplingRate( samplingRate );
   setMaxValue( maxValue );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getPhaseStepPerSample
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double SamplingInfo::getPhaseStepPerSample( double frequency ) const
{
   return 2*M_PI / getPeriodInSamples( frequency );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getPeriodInSamples
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double SamplingInfo::getPeriodInSamples( double frequency ) const
{
   return getSamplingRate() / frequency;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// convertSecsToSamples
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double SamplingInfo::convertSecsToSamples( double periodInSecs ) const
{
   return periodInSecs * getSamplingRate();
}


