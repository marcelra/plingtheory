#include "IRealFuncWithDerivative.h"

namespace Math
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IRealFuncWithDerivative::~IRealFuncWithDerivative()
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// evalDerivMany
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
RealVector IRealFuncWithDerivative::evalDerivMany( const RealVector& xValues ) const
{
   RealVector result( xValues.size() );
   for ( size_t i = 0; i < xValues.size(); ++i )
   {
      result[ i ] = deriv( xValues[ i ] );
   }
   return result;
}

} /// namespace Math
