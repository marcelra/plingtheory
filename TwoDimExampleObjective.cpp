#include "TwoDimExampleObjective.h"

namespace Math
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// evaluate
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double TwoDimExampleObjective::evaluate( const RealVector& x ) const
{
   double r2 = x[0]*x[0] + x[1]*x[1];

   return -r2*r2 + r2 + x[0];
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getNumParameters
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
size_t TwoDimExampleObjective::getNumParameters() const
{
   return 2;
}

} /// namespace Math
