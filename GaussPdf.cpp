#include "GaussPdf.h"

#include <limits>
#include <cmath>

namespace Math
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// constructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
GaussPdf::GaussPdf( double mu, double sigma ) :
   m_mu( mu ),
   m_sigma( sigma ),
   m_argFac( -0.5 / ( sigma * sigma ) ),
   m_norm( 1.0 / sqrt( 2 * M_PI * sigma ) )
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// eval
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double GaussPdf::eval( double x ) const
{
   double arg = ( x - m_mu );
   return m_norm * exp( m_argFac * arg * arg );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getDensity
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double GaussPdf::getDensity( double x ) const
{
   return eval( x );
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMinX
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double GaussPdf::getMinX() const
{
   return -std::numeric_limits< double >::max();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMaxX
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double GaussPdf::getMaxX() const
{
   return std::numeric_limits< double >::max();
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMu
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double GaussPdf::getMu() const
{
   return m_mu;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getSigma
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
double GaussPdf::getSigma() const
{
   return m_sigma;
}

} /// namespace Math
