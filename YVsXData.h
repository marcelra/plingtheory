#ifndef YVSXDATA_H
#define YVSXDATA_H

#include "ITwoDimPlotData.h"

#include <vector>

namespace Plotting
{

/// TODO: doc
class YVsXData : public ITwoDimPlotData
{
   public:
      YVsXData( const std::vector< double >& xData, const std::vector< double >& yData );
      virtual ~YVsXData();

   public:
      double getMinX() const;
      double getMaxX() const;
      double getMinY() const;
      double getMaxY() const;

   public:
      const std::vector< double >& getX() const;
      const std::vector< double >& getY() const;

   private:
      std::vector< double >      m_xData;
      std::vector< double >      m_yData;

      double                     m_xMin;
      double                     m_xMax;
      double                     m_yMin;
      double                     m_yMax;
};



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// Inline methods YVsXData
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMinX
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline double YVsXData::getMinX() const
{
   return m_xMin;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMaxX
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline double YVsXData::getMaxX() const
{
   return m_xMax;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMinY
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline double YVsXData::getMinY() const
{
   return m_yMin;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getMinY
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline double YVsXData::getMaxY() const
{
   return m_yMax;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getX
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline const std::vector< double >& YVsXData::getX() const
{
   return m_xData;
}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// getY
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
inline const std::vector< double >& YVsXData::getY() const
{
   return m_yData;
}

} /// namespace Plotting

#endif // YVSXDATA_H
