#include "IRealFunction.h"

#include <iostream>

namespace Math
{

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// destructor
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
IRealFunction::~IRealFunction()
{}

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/// eval
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
RealVector IRealFunction::evalMany( const RealVector& argVec ) const
{
   RealVector result( argVec.size() );
   for ( size_t i = 0; i < argVec.size(); ++i )
   {
      result[ i ] = ( *this )( argVec[ i ] );
   }
   return result;
}

} /// namespace Math
